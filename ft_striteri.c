#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *s))
{
	int i;

	if (s != NULL && f != NULL)
	{
		i = 0;
		while (s[i] != '\0')
		{
			f(i, s + i);
			i++;
		}
	}
}
