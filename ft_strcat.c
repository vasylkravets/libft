char	*ft_strcat(char *dest, const char *app)
{
	char *ret;

	ret = dest;
	while (*dest != '\0')
	{
		dest++;
	}
	while ((*dest = *app) != '\0')
	{
		dest++;
		app++;
	}
	*dest = '\0';
	return (ret);
}
